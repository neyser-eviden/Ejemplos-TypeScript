// Switch
let dia:number=35
let dato: string

switch (dia) {
    case 1:
        dato = "Lunes"
        break;
    case 2:
        dato = "Lunes"
        break;
    case 3:
        dato = "Miércoles"
        break;
    default:
        dato = "No contemplado"
        break;
}

console.log("Día: ",dato)